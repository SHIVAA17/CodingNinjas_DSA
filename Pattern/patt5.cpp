#include<iostream>   //  Similar number pattern
using namespace std;

int main(){
    int n;          // n for rows
    cin >> n;

    for(int i =1;i<=n;i++){         // i for row number
        for(int j =1;j<=n;j++){      // j for column number
            cout<<j;
        }
        cout << endl;
    }




    return 0;
}
    
    
